import Foundation

import UIKit

extension UIFont {
    
    // MARK: - Montserrat Font
    
    public class func DINCondensedBold(withSize size: CGFloat) -> UIFont {
        return UIFont(name: "DINCondensed-Bold", size: size)!
    }
    public class func SignPainterHouseScriptRegular(withSize size: CGFloat) -> UIFont {
        return UIFont(name: "SignPainterHouseScript-Regular", size: size)!
    }
}
